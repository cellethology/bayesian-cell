def quick_plot(
    results_file="signal_max_comparison_results.csv", plot_type="median", **kwargs
):
    """
    Quick plotting function with customizable options.

    Args:
        results_file: Path to results CSV
        plot_type: Type of plot ("mean", "median", "median_ci", "success", "all")
        **kwargs: Additional arguments for specific plot functions
    """
    results = load_and_prepare_data(results_file)

    if plot_type == "mean":
        return create_mean_plot_with_sem(results, **kwargs)
    elif plot_type == "median":
        # Keep backward compatibility
        return create_mean_plot_with_sem(results, **kwargs)
    elif plot_type == "median_ci":
        return create_median_plot_with_ci(results, **kwargs)
    elif plot_type == "success":
        return create_success_rate_plot(results, **kwargs)
    elif plot_type == "all":
        return create_all_plots(results_file, **kwargs)
    else:
        raise ValueError(f"Unknown plot_type: {plot_type}")


