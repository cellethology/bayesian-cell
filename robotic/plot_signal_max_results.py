def bootstrap_median_ci(data, n_bootstrap=1000, confidence=0.95):
    """
    Calculate bootstrap confidence interval for median.

    Args:
        data: Array-like data
        n_bootstrap: Number of bootstrap samples
        confidence: Confidence level (0.95 for 95% CI)

    Returns:
        tuple: (median, ci_lower, ci_upper)
    """
    if len(data) == 0:
        return np.nan, np.nan, np.nan

    data = np.array(data)
    median_val = np.median(data)

    # Bootstrap samples
    bootstrap_medians = []
    for _ in range(n_bootstrap):
        bootstrap_sample = np.random.choice(data, size=len(data), replace=True)
        bootstrap_medians.append(np.median(bootstrap_sample))

    # Calculate confidence interval
    alpha = 1 - confidence
    ci_lower = np.percentile(bootstrap_medians, 100 * alpha / 2)
    ci_upper = np.percentile(bootstrap_medians, 100 * (1 - alpha / 2))

    return median_val, ci_lower, ci_upper


def load_and_prepare_data(results_file="signal_max_comparison_results.csv"):
    """
    Load raw results and prepare for plotting.

    Args:
        results_file: Path to the raw results CSV file

    Returns:
        pandas.DataFrame: Loaded results
    """
    print(f"Loading data from {results_file}...")

    # Load raw results
    results = pd.read_csv(results_file)

    print(f"Loaded {len(results)} total results")
    print(f"Methods: {results['method'].unique()}")
    print(
        f"Signal max range: {results['signal_max'].min():.1f} to {results['signal_max'].max():.1f}"
    )

    return results


def create_mean_plot_with_sem(
    results_df,
    save_path="signal_max_comparison_plot.pdf",
    figsize=(4.5, 4),
    show_plot=True,
):
    """
    Create line plot showing mean steps to target with SEM error bars for each method.

    Args:
        results_df: Combined results DataFrame
        save_path: Path to save the plot
        figsize: Figure size tuple
        show_plot: Whether to display the plot

    Returns:
        matplotlib Figure object
    """
    plt.figure(figsize=figsize)

    # Color and style for each method
    method_styles = {
        "Standard EKF": {"color": "tab:blue", "marker": "o"},
        "Signal-aware EKF": {"color": "tab:orange", "marker": "o"},
        "Adaptive EKF": {"color": "tab:green", "marker": "o"},
    }

    # Get unique signal_max values and methods
    signal_max_values = sorted(results_df["signal_max"].unique())
    method_names = method_styles.keys()

    print(f"Creating plot for methods: {list(method_names)}")
    print(f"Signal max values: {signal_max_values}")

    # Plot each method
    for method in method_names:
        method_means = []
        method_sems = []
        valid_signal_max = []

        for signal_max in signal_max_values:
            method_data = results_df[
                (results_df["method"] == method)
                & (results_df["signal_max"] == signal_max)
            ]

            # Get successful runs only
            successful_data = method_data[method_data["target_reached"] == True]

            if len(successful_data) > 0:
                steps_data = successful_data["steps_to_target"].values
                mean_val = np.mean(steps_data)
                sem_val = np.std(steps_data) / np.sqrt(
                    len(steps_data)
                )  # Standard Error of Mean

                if not np.isnan(mean_val):
                    method_means.append(mean_val / 1000)  # Convert to thousands
                    method_sems.append(sem_val / 1000)  # Convert to thousands
                    valid_signal_max.append(signal_max)

        if len(method_means) > 0:
            style = method_styles.get(method, {"color": "black", "marker": "o"})

            print(f"Plotting {method}: {len(method_means)} points")

            # Plot mean line with SEM error bars
            plt.errorbar(
                valid_signal_max,
                method_means,
                yerr=method_sems,
                label=method,
                color=style["color"],
                marker=style["marker"],
                linewidth=2,
                markersize=6,
                capsize=3,
                capthick=1,
            )
        else:
            print(f"Warning: No valid data points for {method}")

    # Formatting
    plt.xlabel("Peak Signal Strength", fontsize=12)
    plt.ylabel("Mean Steps to Target (thousands)", fontsize=12)
    # plt.yscale("log")  # Log scale for y-axis
    plt.legend(fontsize=11, frameon=False)
    plt.grid(True, alpha=0.3)

    # Remove top and right spines
    plt.gca().spines["top"].set_visible(False)
    plt.gca().spines["right"].set_visible(False)
    # Remove grid
    plt.grid(False)

    # Improve layout
    plt.tight_layout()

    # Save plot
    plt.savefig(save_path, dpi=300, bbox_inches="tight", format="pdf")
    print(f"Plot saved to {save_path}")

    # Show plot
    if show_plot:
        plt.show()

    return plt.gcf()


def create_median_plot_with_ci(
    results_df,
    save_path="signal_max_median_comparison_plot.pdf",
    figsize=(4.5, 4),
    show_plot=True,
    confidence=0.95,
):
    """
    Create line plot showing median steps to target with CI error bars for each method.

    Args:
        results_df: Combined results DataFrame
        save_path: Path to save the plot
        figsize: Figure size tuple
        show_plot: Whether to display the plot
        confidence: Confidence level for bootstrap CI (default 0.95)

    Returns:
        matplotlib Figure object
    """
    plt.figure(figsize=figsize)

    # Color and style for each method
    method_styles = {
        "Standard EKF": {"color": "tab:blue", "marker": "o"},
        "Signal-aware EKF": {"color": "tab:orange", "marker": "o"},
        "Adaptive EKF": {"color": "tab:green", "marker": "o"},
    }

    # Get unique signal_max values and methods
    signal_max_values = sorted(results_df["signal_max"].unique())
    method_names = method_styles.keys()

    print(f"Creating median plot for methods: {list(method_names)}")
    print(f"Signal max values: {signal_max_values}")

    # Plot each method
    for method in method_names:
        method_medians = []
        ci_lowers = []
        ci_uppers = []
        valid_signal_max = []

        for signal_max in signal_max_values:
            method_data = results_df[
                (results_df["method"] == method)
                & (results_df["signal_max"] == signal_max)
            ]

            # Get successful runs only
            successful_data = method_data[method_data["target_reached"] == True]

            if len(successful_data) > 0:
                steps_data = successful_data["steps_to_target"].values

                # Calculate median and bootstrap CI
                median_val, ci_lower, ci_upper = bootstrap_median_ci(
                    steps_data, confidence=confidence
                )

                if not np.isnan(median_val):
                    method_medians.append(median_val / 1000)  # Convert to thousands
                    ci_lowers.append(ci_lower / 1000)  # Convert to thousands
                    ci_uppers.append(ci_upper / 1000)  # Convert to thousands
                    valid_signal_max.append(signal_max)

        if len(method_medians) > 0:
            style = method_styles.get(method, {"color": "black", "marker": "o"})

            print(f"Plotting {method}: {len(method_medians)} points")

            # Calculate error bar values (asymmetric)
            yerr_lower = [
                method_medians[i] - ci_lowers[i] for i in range(len(method_medians))
            ]
            yerr_upper = [
                ci_uppers[i] - method_medians[i] for i in range(len(method_medians))
            ]

            # Plot median line with CI error bars
            plt.errorbar(
                valid_signal_max,
                method_medians,
                yerr=[yerr_lower, yerr_upper],
                label=method,
                color=style["color"],
                marker=style["marker"],
                linewidth=2,
                markersize=6,
                capsize=3,
                capthick=1,
            )
        else:
            print(f"Warning: No valid data points for {method}")

    # Formatting
    plt.xlabel("Peak Signal Strength", fontsize=12)
    plt.ylabel("Median Steps to Target (thousands)", fontsize=12)
    # plt.yscale("log")  # Log scale for y-axis
    plt.legend(fontsize=11, frameon=False)
    plt.grid(True, alpha=0.3)

    # Remove top and right spines
    plt.gca().spines["top"].set_visible(False)
    plt.gca().spines["right"].set_visible(False)
    # Remove grid
    plt.grid(False)

    # Improve layout
    plt.tight_layout()

    # Save plot
    plt.savefig(save_path, dpi=300, bbox_inches="tight", format="pdf")
    print(f"Median plot with CI saved to {save_path}")

    # Show plot
    if show_plot:
        plt.show()

    return plt.gcf()


def create_success_rate_plot(
    results_df,
    save_path="signal_max_success_rates.png",
    figsize=(10, 6),
    show_plot=True,
):
    """
    Create success rate comparison plot.

    Args:
        results_df: Combined results DataFrame
        save_path: Path to save the plot
        figsize: Figure size tuple
        show_plot: Whether to display the plot

    Returns:
        matplotlib Figure object
    """
    plt.figure(figsize=figsize)

    # Method styles
    method_styles = {
        "Standard EKF": {"color": "tab:blue", "marker": "o", "linestyle": "-"},
        "Signal-aware EKF": {"color": "tab:orange", "marker": "s", "linestyle": "--"},
        "Adaptive EKF": {"color": "tab:green", "marker": "^", "linestyle": "-."},
        "Adaptive Process EKF": {
            "color": "tab:green",
            "marker": "^",
            "linestyle": "-.",
        },
        "Adaptive Measurement EKF": {
            "color": "tab:orange",
            "marker": "s",
            "linestyle": "--",
        },
    }

    # Calculate success rates
    success_rates = (
        results_df.groupby(["signal_max", "method"])["target_reached"]
        .mean()
        .reset_index()
    )

    # Plot each method
    for method in results_df["method"].unique():
        method_data = success_rates[success_rates["method"] == method].sort_values(
            "signal_max"
        )
        style = method_styles.get(
            method, {"color": "black", "marker": "o", "linestyle": "-"}
        )

        plt.plot(
            method_data["signal_max"],
            method_data["target_reached"] * 100,  # Convert to percentage
            label=method,
            color=style["color"],
            marker=style["marker"],
            linestyle=style["linestyle"],
            linewidth=2,
            markersize=6,
            alpha=0.8,
        )

    plt.xlabel("Peak Signal Strength", fontsize=12)
    plt.ylabel("Success Rate (%)", fontsize=12)
    plt.title("Success Rate Across Signal Strengths", fontsize=14)
    plt.legend(fontsize=11)
    plt.ylim(0, 105)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()

    plt.savefig(save_path, dpi=300, bbox_inches="tight")
    print(f"Success rate plot saved to {save_path}")

    if show_plot:
        plt.show()

    return plt.gcf()


