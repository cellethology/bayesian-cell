"""
Plotting functions for signal decay comparison results.
Reads CSV files generated by signal_decay_comparison.py and creates customizable plots.
"""

import numpy as np
import matplotlib.pyplot as plt
import pandas as pd
import seaborn as sns


def load_and_prepare_data(results_file="signal_decay_comparison_results.csv"):
    """
    Load raw results and compute summary statistics.

    Args:
        results_file: Path to the raw results CSV file

    Returns:
        tuple: (raw_results_df, summary_stats_df)
    """
    print(f"Loading data from {results_file}...")

    # Load raw results
    results = pd.read_csv(results_file)

    # Create summary statistics
    summary_stats = (
        results.groupby(["signal_max", "method"])
        .agg({"steps_to_target": ["mean", "std", "count"], "target_reached": "mean"})
        .reset_index()
    )

    # Flatten column names
    summary_stats.columns = [
        "signal_max",
        "method",
        "mean_steps",
        "std_steps",
        "count",
        "success_rate",
    ]

    # Calculate standard error for error bars
    summary_stats["sem_steps"] = summary_stats["std_steps"] / np.sqrt(
        summary_stats["count"]
    )

    print(f"Loaded {len(results)} total results")
    print(f"Methods: {results['method'].unique()}")
    print(
        f"Signal max range: {results['signal_max'].min():.4f} to {results['signal_max'].max():.4f}"
    )

    return results, summary_stats


def create_main_line_plot(
    summary_stats,
    save_path="signal_decay_comparison_plot.pdf",
    figsize=(4.5, 4),
    show_error_bars=True,
):
    """
    Create the main line plot showing mean steps to target vs signal max.

    Args:
        summary_stats: DataFrame with summary statistics
        save_path: Path to save the plot
        figsize: Figure size tuple
        show_error_bars: Whether to show error bars
    """
    plt.figure(figsize=figsize)

    # Color and style for each method
    method_styles = {
        "Standard KF": {"color": "tab:blue", "marker": "o"},
        "Signal-aware KF": {"color": "tab:orange", "marker": "o"},
        "Adaptive KF": {"color": "tab:green", "marker": "o"},
    }

    # Plot each method
    for method in ["Standard KF", "Signal-aware KF", "Adaptive KF"]:
        method_data = summary_stats[summary_stats["method"] == method].sort_values(
            "signal_max"
        )

        style = method_styles.get(method, {"color": "black", "marker": "o"})

        # Calculate signal decay length (reciprocal of signal_decay)
        signal_decay_length = method_data["signal_max"]

        if show_error_bars:
            plt.errorbar(
                signal_decay_length,
                method_data["mean_steps"],
                yerr=method_data["sem_steps"],
                label=method,
                color=style["color"],
                marker=style["marker"],
                linewidth=2,
                markersize=4,
                capsize=3,
            )
        else:
            plt.plot(
                signal_decay_length,
                method_data["mean_steps"],
                label=method,
                color=style["color"],
                marker=style["marker"],
                linewidth=2,
                markersize=4,
            )

    # Formatting
    plt.xlabel("Peak Signal Strength", fontsize=12)
    plt.ylabel("Mean Steps to Target", fontsize=12)
    plt.legend(fontsize=11, frameon=False)
    plt.gca().spines["top"].set_visible(False)
    plt.gca().spines["right"].set_visible(False)

    # Improve layout
    plt.tight_layout()

    # Save plot
    plt.savefig(save_path, dpi=300, bbox_inches="tight", format="pdf")
    print(f"Main line plot saved to {save_path}")

    return plt.gcf()


def create_success_rate_plot(
    summary_stats, save_path="signal_decay_success_rates.png", figsize=(5, 4)
):
    """
    Create success rate comparison plot.

    Args:
        summary_stats: DataFrame with summary statistics
        save_path: Path to save the plot
        figsize: Figure size tuple
    """
    plt.figure(figsize=figsize)

    method_styles = {
        "Standard KF": {"color": "blue", "marker": "o", "linestyle": "-"},
        "Signal-aware KF": {"color": "red", "marker": "o", "linestyle": "-"},
        "Adaptive KF": {"color": "green", "marker": "o", "linestyle": "-"},
    }

    for method in summary_stats["method"].unique():
        method_data = summary_stats[summary_stats["method"] == method].sort_values(
            "signal_max"
        )
        style = method_styles.get(
            method, {"color": "black", "marker": "o", "linestyle": "-"}
        )

        plt.plot(
            method_data["signal_max"],
            method_data["success_rate"] * 100,  # Convert to percentage
            label=method,
            color=style["color"],
            marker=style["marker"],
            linestyle=style["linestyle"],
            linewidth=2,
            markersize=6,
            alpha=0.8,
        )

    plt.xlabel("Signal Max", fontsize=12)
    plt.ylabel("Success Rate (%)", fontsize=12)
    plt.title("Success Rate Across Signal Max Values", fontsize=14)
    plt.legend(fontsize=11)
    plt.ylim(0, 105)
    plt.tight_layout()

    plt.savefig(save_path, dpi=300, bbox_inches="tight")
    print(f"Success rate plot saved to {save_path}")

    return plt.gcf()


def create_all_plots(
    results_file="signal_decay_comparison_results.csv", show_plots=False
):
    """
    Create all available plots from the results file.

    Args:
        results_file: Path to the raw results CSV file
        show_plots: Whether to display plots (in addition to saving)
    """
    print("=" * 60)
    print("CREATING ALL PLOTS FROM SAVED RESULTS")
    print("=" * 60)

    # Load data
    results, summary_stats = load_and_prepare_data(results_file)

    # Create all plots
    print("\nCreating plots...")

    # Main line plots
    create_main_line_plot(summary_stats, "signal_decay_comparison_plot.png")

    # Success rate plot
    create_success_rate_plot(summary_stats)

    print("\n" + "=" * 60)
    print("ALL PLOTS CREATED")
    print("=" * 60)
    print("Files created:")
    print("  - signal_decay_comparison_plot.png (main plot)")
    print("  - signal_decay_success_rates.png (success rate comparison)")

    if show_plots:
        plt.show()


def quick_plot(
    results_file="signal_decay_comparison_results.csv", plot_type="main", **kwargs
):
    """
    Quick plotting function with customizable options.

    Args:
        results_file: Path to results CSV
        plot_type: Type of plot ("main", "success", "heatmap", "boxplot")
        **kwargs: Additional arguments for specific plot functions
    """
    results, summary_stats = load_and_prepare_data(results_file)

    if plot_type == "main":
        return create_main_line_plot(summary_stats, **kwargs)
    else:
        raise ValueError(f"Unknown plot_type: {plot_type}")


if __name__ == "__main__":
    # Create all plots when run directly
    quick_plot(plot_type="main")
